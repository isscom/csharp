// Задача 27: Напишите программу, которая принимает на
// вход число и выдаёт сумму цифр в числе.

// 452 -> 11
// 82 -> 10
// 9012 -> 12

//Инициализируем метод:
//НАЧАЛО МЕТОДА
int nsumm(int number) // функция nsumm с параметром number
{
    int result = number%10; // вводим переменную, куда будем складывать по 1 цифре из введенного числа. Изначально это остаток от деления на 10 (последняя цифра)
    while (number > 10) // пока введенный параметр > 10 - выполняем действия (пока число не станет двузначным)
    {
        number = number / 10; // убираем последнюю цифру
        result = result + number %10; // прибавляем новую последнюю цифру к результату
    }
    return result; // возвращаем результат после отработки условий
}
//КОНЕЦ МЕТОДА

Console.WriteLine("Введите число: ");
int N = int.Parse(Console.ReadLine());
