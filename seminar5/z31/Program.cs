int sumPos = 0; // Вводим переменную, которая будет складывать положительные числа
int sumNeg = 0; // Переменная, складывающая отрицательные числа

Console.WriteLine(""); // Пустая строка

for (int i = 0; i < mass.Length; i++) // Проходим по элементам массива
{
    if (mass[i] > 0) // Ищем положительные
    {
        sumPos += mass[i]; // Складываем их
    }
    else
    {
        sumNeg += mass[i]; // Иначе (если число отрицательное) складываем в отрицательные
    }
}

Console.WriteLine("Сумма положительных чисел равна: " + sumPos); // Выводим сумму положительных
Console.WriteLine("Сумма отрицательных чисел равна: " + sumNeg); //Выводим сумму отрицательных