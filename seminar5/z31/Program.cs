// Задача 31: Задайте массив из 12 элементов, заполненный
// случайными числами из промежутка [-9, 9]. Найдите сумму
// отрицательных и положительных элементов массива.

// Например, в массиве [3,9,-8,1,0,-7,2,-1,8,-3,-1,6] сумма
// положительных чисел равна 29, сумма отрицательных равна
// -20.

void Print(int[] arr) // Метод для вывода на экран массива
{
    for (int i = 0; i < arr.Length; i++)
{
    Console.Write(arr[i] + " ");
}
}
//КОНЕЦ МЕТОДА

int[] mass = new int[12]; // Вводим массив из 12 элементов
for (int i = 0; i < mass.Length; i++) // Проходим по каждому элементу
{
    mass[i] = new Random().Next(-9, 10); // Цикл отвечает за генерацию всех элементов (массива) числами от -9 до 9
}

Print(mass); // Вызываем метод (цикл ниже закомментирован, скопирован для функции)

